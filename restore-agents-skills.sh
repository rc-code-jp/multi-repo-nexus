# Init Repo スキル

このスキルは、複数のリポジトリを持つプロジェクトの親となるリポジトリを初期化します。
Codex のルールや補助ファイルを親ディレクトリで一括管理し、環境構築や横断したタスクを進めやすい構成を提供します。

## When to Use

- 新しいマルチリポジトリプロジェクトをセットアップするとき
- 既存のプロジェクトを親リポジトリで管理する構成に変更するとき
- 子フォルダに `.git` ディレクトリを置いて独立したリポジトリとして扱い、Codex からは読み取り可能にしたいとき
- 複数のサブプロジェクトを統括する親プロジェクトの環境を構築するとき

## Instructions

### 0. 初期化完了状態のチェック

最初に `.agents/skills/init-repo/.completed` ファイルの存在を確認する。

- ファイルが存在する場合
  - ユーザーに「このリポジトリは既に初期化済みです。`.completed` ファイルが存在します。」と伝える
  - このスキルの実行を終了し、以降のステップは実行しない
- ファイルが存在しない場合
  - 以下のステップ1から順に実行する

### 1. リポジトリの状態確認

- 現在のディレクトリが Git リポジトリであることを確認する
- `.git` ディレクトリの存在を確認する
- 既存のプロジェクトフォルダやサブリポジトリの有無を確認する
- ルートの `AGENTS.md` を確認し、プロジェクト固有の指示に従う

### 2. 子リポジトリ候補の確認

ルート直下のディレクトリを確認し、子リポジトリとして扱う対象をユーザーに確認する。

- 既に存在する子フォルダを列挙する
- どのフォルダを独立したリポジトリとして扱うか確認する
- 子リポジトリの Codex 設定ファイル（`AGENTS.md` など）には変更を加えない

### 3. 子フォルダの Git 初期化確認

対象の子フォルダごとに `.git` ディレクトリの存在を確認する。

- `.git` が存在する場合
  - 既に独立した子リポジトリとして扱われているため、内容を変更しない
- `.git` が存在しない場合
  - ユーザーに確認したうえで、対象フォルダ内で `git init` を実行する
  - 既存のリモートリポジトリがある場合は、`git clone` を使って子フォルダを作成する

確認コマンド例：

```zsh
ls -la project-a/.git
```

初期化コマンド例：

```zsh
cd project-a && git init
```

### 4. 環境変数ファイルの設定

`.env.example` ファイルの存在を確認する。

- `.env.example` が存在する場合
  - `.env` がまだ存在しないことを確認してから、以下のコマンドで `.env` ファイルを作成する

```zsh
cp .env.example .env
```

  - ユーザーに `.env` ファイルの編集が必要かどうかを確認し、必要に応じて編集を促す
- `.env.example` が存在しない場合
  - ユーザーに「`.env.example` ファイルが存在しないため、このステップをスキップします」と伝える
  - 次のステップに進む

### 5. サブプロジェクトのクローン

必要なサブプロジェクトがまだ存在しない場合は、ユーザーに各サブプロジェクトの Git リポジトリ URL を確認する。
URL が提供された場合、以下の形式でクローンする。

```zsh
git clone <repository-url> <project-name>
```

注意事項：

- URL が提供されない場合、このステップをスキップする
- 既にディレクトリが存在する場合はクローンをスキップする
- プライベートリポジトリの場合、認証が必要になることをユーザーに伝える

### 6. Git 状態の確認

親リポジトリと対象の子フォルダで `git status` を確認し、管理範囲が分離されていることを確認する。

補足: Git は配下に `.git` ディレクトリがあるフォルダを別リポジトリとして扱い、その内部ファイルを親リポジトリの通常の追跡対象にしない。

### 7. 初期化の完了確認

以下の各項目について、ファイルの存在やコマンド実行結果を確認し、全て完了していることを検証する。

1. 子リポジトリの `.git` 確認
   - 対象の子フォルダに `.git` ディレクトリが存在する
2. `.env` ファイルの存在確認（`.env.example` があった場合）
   - `ls` コマンドなどでファイルが存在することを確認する
3. サブプロジェクトのクローン確認（URL が提供された場合）
   - `ls` コマンドでサブプロジェクトのディレクトリが存在することを確認する
4. Git 管理範囲の確認
   - 親リポジトリと子リポジトリで `git status` を確認し、管理範囲が分離されていることを確認する

### 8. 次のステップの案内

ユーザーに以下の次のステップを案内する。

1. サブプロジェクトを追加する場合は、子フォルダを作成して `git init` または `git clone` を実行する
2. Codex から全てのサブプロジェクトが読み取り可能であることを確認する

### 9. 完了マーカーファイルの作成

全ての初期化ステップ（ステップ1〜8）が正常に完了したことを確認したら、以下のコマンドを実行し、`.completed` ファイルを作成する。

```zsh
touch .agents/skills/init-repo/.completed
```

このファイルの役割：

- 初期化が完了したことを示すマーカーファイル
- 次回このスキルが実行された際、ステップ0で初期化済みであることを検出できる
- `.gitignore` で Git 管理から除外されているため、ローカル環境のみで管理される

## Additional Notes

- このボイラーテンプレートは、Codex を中心にした AI コーディング運用を想定している
- 子フォルダに `.git` ディレクトリを置くことで、サブプロジェクトを独立した Git リポジトリとして扱いつつ、Codex からは読み取り可能にできる
- マルチリポジトリ構成により、各サブプロジェクトは独立した Git リポジトリとして管理できる
- 親リポジトリで共通のルールや設定を一括管理できるため、プロジェクト全体の一貫性を保ちやすい

## Troubleshooting

### 子フォルダが親の Git 管理対象に見える

対象フォルダ内に `.git` ディレクトリが存在するか確認する。

```zsh
ls -la project-a/.git
```

`.git` が存在しない場合は、対象フォルダ内で初期化する。

```zsh
cd project-a && git init
```

### Git 管理範囲が期待と異なる

1. 親リポジトリのルートで `git status` を確認する
2. 子フォルダ内で `git status` を確認する
3. 作業しているコマンドの実行場所が親か子かを確認する

### サブプロジェクトが Codex から読み取れない

- Codex がワークスペース全体を読み取れるように設定されているか確認する
- サブプロジェクトのディレクトリが実際に存在するか確認する
