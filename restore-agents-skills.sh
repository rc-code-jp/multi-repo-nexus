# Project Tree スキル

プロジェクトのディレクトリ構造を視覚的なツリー形式で表示します。

## When to Use

- プロジェクトの全体構造を把握したいとき
- ディレクトリの階層構造を確認したいとき
- 特定のフォルダ配下のファイル一覧を確認したいとき
- プロジェクトのドキュメントや README に構造を記載したいとき

## Instructions

1. 対象ディレクトリの特定
   - ユーザーが指定したパス、またはワークスペースのルートディレクトリを対象とする
   - 相対パスが指定された場合はワークスペースルートからの相対パスとして解釈する
2. ディレクトリ構造の取得
   - `rg --files`、`find`、`ls` など、利用可能なコマンドでディレクトリの内容を取得する
   - 隠しファイル・フォルダ（`.` で始まるもの）はデフォルトで除外する
   - `node_modules`、`.git`、`__pycache__`、`.venv`、`dist`、`build` などの一般的な除外対象は自動的にスキップする
3. ツリー形式での出力
   - 以下の記号を使用してツリー構造を表現する
     - `├──` : 同階層に続く要素がある場合
     - `└──` : 同階層の最後の要素
     - `│   ` : 上位階層からの接続線
     - `    ` : 空白（接続線なし）
   - ディレクトリ名の末尾には `/` を付けて区別する
   - ファイルとディレクトリはアルファベット順でソートし、ディレクトリを先に表示する
4. 出力例

```text
project-name/
├── src/
│   ├── components/
│   │   ├── Button.tsx
│   │   └── Header.tsx
│   ├── utils/
│   │   └── helpers.ts
│   └── index.ts
├── tests/
│   └── example.test.ts
├── package.json
└── README.md
```

5. オプション対応
   - 深さ制限：指定された階層までのみ表示する
   - 除外パターン：特定のファイルやフォルダを除外する
   - ファイルのみ、またはディレクトリのみの表示に対応する
   - 隠しファイルの表示切り替えに対応する
6. 大規模プロジェクトへの対応
   - ファイル数が多い場合は、最初に概要（ディレクトリのみ）を表示してから詳細を確認する
   - 必要に応じて特定のサブディレクトリに絞って表示する
EOF

